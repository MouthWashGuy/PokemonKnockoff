package pokemongame.pokemove;

import pokemongame.typing.PokeType.ALL_TYPES;

public abstract class PokeMove {
    
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // INSTANCE VARIABLES
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    private String name;
    private ALL_TYPES type;
    private int priority;
    private int PP;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // CONSTRUCTOR
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public PokeMove(String name, ALL_TYPES type, int priority, int PP) {
        this.name = name;
        this.type = type;
        this.priority = priority;
        this.PP = PP;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    // GETTERS
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    
}
